a = int(input("Digite o primeiro número: "))
b = int(input("Digite o segundo número: "))
soma = a + b # Soma
produto = a * b # Multiplicação
subtracao = a - b # Subtração
divisao = a / b # Divisao
print("Soma:", soma)
print("Produto:", produto)
print("Subtracao:", subtracao)
print("Divisao:", divisao)